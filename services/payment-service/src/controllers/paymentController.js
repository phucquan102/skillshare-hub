async function handlePaymentIntentSucceeded(paymentIntent) {
  try {
    console.log('üí∞ Payment succeeded:', paymentIntent.id);
    
    // T√¨m payment record trong database
    const payment = await Payment.findOne({ transactionId: paymentIntent.id });
    if (payment) {
      // üî• QUAN TR·ªåNG: Ki·ªÉm tra enrollment tr∆∞·ªõc khi c·∫≠p nh·∫≠t
      const enrollmentCheck = await checkUserEnrollment(
        payment.userId, 
        payment.courseId, 
        payment.lessonId
      );
      
      if (enrollmentCheck.blockPayment) {
        console.log('‚ùå [WEBHOOK BLOCKED] User already enrolled, marking payment as failed');
        payment.paymentStatus = 'failed';
        payment.failedAt = new Date();
        await payment.save();
        
        // üî• HO√ÄN TI·ªÄN T·ª∞ ƒê·ªòNG n·∫øu ƒë√£ mua r·ªìi
        try {
          await stripe.refunds.create({
            payment_intent: paymentIntent.id,
            reason: 'duplicate'
          });
          console.log('‚úÖ Automatic refund processed for duplicate purchase');
        } catch (refundError) {
          console.error('‚ùå Automatic refund failed:', refundError);
        }
        
        return;
      }
      
      payment.paymentStatus = 'completed';
      payment.completedAt = new Date();
      await payment.save();
      
      console.log('‚úÖ Payment status updated to completed:', payment._id);
      
      // TODO: G·ªçi enrollment service ƒë·ªÉ t·∫°o enrollment
    } else {
      console.warn('‚ö†Ô∏è No payment record found for Stripe payment intent:', paymentIntent.id);
    }
  } catch (error) {
    console.error('‚ùå Error handling payment success:', error);
  }
}

